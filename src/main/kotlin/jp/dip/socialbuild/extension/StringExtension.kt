package jp.dip.socialbuild.extension

import org.bukkit.ChatColor

/**
 * Created by unhappychoice on 2015/05/24.
 */

public fun String.blue(): String {
    return ChatColor.BLUE.toString() + this
}

public fun String.green(): String {
    return ChatColor.GREEN.toString() + this
}

public fun String.red(): String {
    return ChatColor.RED.toString() + this
}

public fun String.darkAqua(): String {
    return ChatColor.DARK_AQUA.toString() + this
}

public fun String.yellow(): String {
    return ChatColor.YELLOW.toString() + this
}

